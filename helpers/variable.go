package helpers

func variableFunc() {
	var name string

	name = "yosua"

	println("name => ", name)

	var car = "Toyota"

	println("%s", car)

	var age = 20
	println("age => ", age)

	job := "programmer"
	println("job => ", job)

	// multiple variable
	var (
		dob   = "15-08-2001"
		class = 6
	)
	println(dob, class)
}
